// Enhanced translation function
function translatePageComprehensive(lang) {
    if (lang === 'en') return;
    
    const langTranslations = comprehensiveTranslations[lang];
    if (!langTranslations) return;

    // Translate all text elements
    document.querySelectorAll('*').forEach(element => {
        if (element.children.length === 0) {
            const text = element.textContent.trim();
            if (text && langTranslations[text]) {
                element.textContent = langTranslations[text];
            }
        }
        
        // Translate placeholders
        if (element.placeholder && langTranslations[element.placeholder]) {
            element.placeholder = langTranslations[element.placeholder];
        }
        
        // Translate option values
        if (element.tagName === 'OPTION' && langTranslations[element.textContent.trim()]) {
            element.textContent = langTranslations[element.textContent.trim()];
        }
    });
    
    // Store current language for dynamic content
    window.currentLanguage = lang;
}

// Function to translate dynamic content
function translateDynamicContent() {
    const lang = window.currentLanguage || localStorage.getItem('selectedLanguage') || 'en';
    if (lang === 'en') return;
    
    const langTranslations = comprehensiveTranslations[lang];
    if (!langTranslations) return;
    
    // Translate dynamic fields specifically
    document.querySelectorAll('#dynamicFields *').forEach(element => {
        if (element.children.length === 0) {
            const text = element.textContent.trim();
            if (text && langTranslations[text]) {
                element.textContent = langTranslations[text];
            }
        }
        
        if (element.placeholder && langTranslations[element.placeholder]) {
            element.placeholder = langTranslations[element.placeholder];
        }
        
        if (element.tagName === 'OPTION' && langTranslations[element.textContent.trim()]) {
            element.textContent = langTranslations[element.textContent.trim()];
        }
    });
}

        // Auto-apply translation on page load
document.addEventListener('DOMContentLoaded', function() {
    const savedLang = localStorage.getItem('selectedLanguage') || 'en';
    translatePageComprehensive(savedLang);
    
    // Set up observer for dynamic content
    const observer = new MutationObserver(function(mutations) {
        mutations.forEach(function(mutation) {
            if (mutation.type === 'childList' && mutation.addedNodes.length > 0) {
                // Check if dynamic fields were added
                const dynamicFields = document.getElementById('dynamicFields');
                if (dynamicFields && mutation.target.contains && mutation.target.contains(dynamicFields)) {
                    setTimeout(translateDynamicContent, 100);
                }
            }
        });
    });
    
    observer.observe(document.body, {
        childList: true,
        subtree: true
    });
});